    Key? key,
    required this.id,
    required this.image,
    required this.email,
    required this.nom,
    required this.tel,
    required this.adresse,
    required this.idus,
    required this.url,
    required this.emailus,
    required this.nameus,
    required this.roleus,
    required this.accesus,
    required this.telus,
    required this.adrus,
  }) : super(key: key);

  @override
  State<ContactDetail> createState() => _ContactDetailState();
}

